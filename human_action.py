# Initialize MediaPipe Pose
mp_pose = mp.solutions.pose
pose = mp_pose.Pose()
mp_drawing = mp.solutions.drawing_utils
drawing_utils = mp.solutions.drawing_utils

mp_hands = mp.solutions.hands
hands = mp_hands.Hands()

play_pause_state = False  # False means paused, True means playing
prev_dist = 0
threshold = 50
change_delay = 5
frame_count = 0
def detect_action(landmarks):
    if not landmarks:
        return "Unknown"

    left_wrist = landmarks[mp_pose.PoseLandmark.LEFT_WRIST.value]
    left_shoulder = landmarks[mp_pose.PoseLandmark.LEFT_SHOULDER.value]
    right_wrist = landmarks[mp_pose.PoseLandmark.RIGHT_WRIST.value]
    right_shoulder = landmarks[mp_pose.PoseLandmark.RIGHT_SHOULDER.value]
    left_elbow = landmarks[mp_pose.PoseLandmark.LEFT_ELBOW.value]
    right_elbow = landmarks[mp_pose.PoseLandmark.RIGHT_ELBOW.value]
    left_hip = landmarks[mp_pose.PoseLandmark.LEFT_HIP.value]
    right_hip = landmarks[mp_pose.PoseLandmark.RIGHT_HIP.value]
    left_knee = landmarks[mp_pose.PoseLandmark.LEFT_KNEE.value]
    right_knee = landmarks[mp_pose.PoseLandmark.RIGHT_KNEE.value]

    torso_vertical = abs(left_shoulder.x - left_hip.x) < 0.1 and abs(right_shoulder.x - right_hip.x) < 0.1
    legs_straight = abs(left_knee.y - left_hip.y) > 0.3 and abs(right_knee.y - right_hip.y) > 0.3
    arms_neutral = left_wrist.y > left_shoulder.y and right_wrist.y > right_shoulder.y

    if torso_vertical and legs_straight and arms_neutral:
        return "Standing Pose"
    if left_wrist.y < left_elbow.y - 0.1 and left_wrist.y < left_shoulder.y - 0.1:
        return "Left Arm Stretching"
    if right_wrist.y < right_elbow.y - 0.1 and right_wrist.y < right_shoulder.y - 0.1:
        return "Right Hand Stretching"
    if left_wrist.y < left_shoulder.y:
        return "Left Hand Raised"
    if right_wrist.y < right_shoulder.y:
        return "Right Hand Raised"
    if (abs(left_wrist.y - left_shoulder.y) < 0.05 and abs(right_wrist.y - right_shoulder.y) < 0.05 and
        left_wrist.x < left_shoulder.x and right_wrist.x > right_shoulder.x):
        return "T-Pose"

    return "Unknown"

def process_frame(frame):
    img_rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
    pose_result = pose.process(img_rgb)
    action = "Unknown"

    if pose_result.pose_landmarks:
        landmarks = pose_result.pose_landmarks.landmark
        action = detect_action(landmarks)
        mp_drawing.draw_landmarks(frame, pose_result.pose_landmarks, mp_pose.POSE_CONNECTIONS)

    cv2.putText(frame, f'Action: {action}', (10, 30), cv2.FONT_HERSHEY_SIMPLEX, 1,(0, 0, 0), 2)
    return frame

def video_feed():
    cap = cv2.VideoCapture(0)

    while cap.isOpened():
        ret, frame = cap.read()
        if not ret:
            break

        frame = process_frame(frame)
        _, buffer = cv2.imencode('.jpg', frame)
        frame_bytes = buffer.tobytes()

        yield (b'--frame\r\n'
               b'Content-Type: image/jpeg\r\n\r\n' + frame_bytes + b'\r\n')

    cap.release()

def video_stream(request):
    return StreamingHttpResponse(video_feed(), content_type="multipart/x-mixed-replace;boundary=frame")

