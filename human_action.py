def detect_gestures():
    """Captures webcam frames, processes hand gestures, and sends video stream."""
    global play_pause_state, prev_dist, frame_count

    cap = cv2.VideoCapture(0)  # Open webcam
    while cap.isOpened():
        ret, frame = cap.read()
        if not ret:
            break

        frame = cv2.flip(frame, 1)
        rgb_frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        output = hands.process(rgb_frame)

        if output.multi_hand_landmarks and output.multi_handedness:
            for hand_landmarks, hand_label in zip(output.multi_hand_landmarks, output.multi_handedness):
                drawing_utils.draw_landmarks(frame, hand_landmarks, mp_hands.HAND_CONNECTIONS)
                landmarks = hand_landmarks.landmark
                hand_type = hand_label.classification[0].label  # 'Left' or 'Right'

                # Right Hand: Play/Pause
                if hand_type == 'Right':
                    index_finger_up = landmarks[8].y < landmarks[7].y
                    middle_finger_up = landmarks[12].y < landmarks[11].y

                    if index_finger_up and not middle_finger_up:  # Pause
                        if play_pause_state:
                            pyautogui.press("playpause")
                            play_pause_state = False

                    elif index_finger_up and middle_finger_up:  # Play
                        if not play_pause_state:
                            pyautogui.press("playpause")
                            play_pause_state = True

                # Left Hand: Volume Control
                elif hand_type == 'Left':
                    x1, y1 = int(landmarks[4].x * frame.shape[1]), int(landmarks[4].y * frame.shape[0])  # Thumb
                    x2, y2 = int(landmarks[8].x * frame.shape[1]), int(landmarks[8].y * frame.shape[0])  # Index Finger

                    dist = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5 // 4
                    cv2.line(frame, (x1, y1), (x2, y2), (0, 255, 0), 5)

                    frame_count += 1
                    if frame_count > change_delay:
                        if dist > threshold and dist > prev_dist:
                            pyautogui.press("volumeup")
                        elif dist < threshold and dist < prev_dist:
                            pyautogui.press("volumedown")
                        frame_count = 0
                        prev_dist = dist

        # Convert frame to JPEG and yield as response
        _, buffer = cv2.imencode('.jpg', frame)
        frame_bytes = buffer.tobytes()
        yield (b'--frame\r\n'
               b'Content-Type: image/jpeg\r\n\r\n' + frame_bytes + b'\r\n')

    cap.release()
